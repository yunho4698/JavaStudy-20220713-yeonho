package j03_연산자;

//Ctrl + space ->자동완성
public class Operation1 {

	public static void main(String[] args) {
		int num = 50;
		System.out.println(++num);
		System.out.println(num);
		System.out.println(num++);
		System.out.println(num);
		
		//위에거 잡고 Ctrl+Atl+아래방향키 하면 위에거 복사되서 밑에 나옴 
		//한줄은 그냥 커서만 있으면 되고 여러줄은 다 같이 잡아서 실행
		System.out.println(--num);
		System.out.println(num);
		System.out.println(num--);
		System.out.println(num);

		
		
	}

}
